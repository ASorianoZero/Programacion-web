/*
    El ciclo Do While ejecuta un código al menos una vez y si se cumple la condición volverá a iterarlo.
*/

var i = 0;
var numero = prompt("Pon tu inche número");

do {
    //Aquí el código que se ejecuta en el ciclo.
    document.write("Archibaldo <br>" );
    i++;
}   while ( 1 < numero);
